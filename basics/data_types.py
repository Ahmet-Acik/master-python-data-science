"""
data_types.py
-------------
Covers Python built-in data types: int, float, str, bool, list, tuple, dict, set.
"""

def data_type_examples():
    integer = 10
    floating = 3.14
    string = "hello"
    boolean = True
    my_list = [1, 2, 3]
    my_tuple = (1, 2, 3)
    my_dict = {'a': 1, 'b': 2}
    my_set = {1, 2, 3}
    return integer, floating, string, boolean, my_list, my_tuple, my_dict, my_set
